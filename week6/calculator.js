function add(x, y) {
    return x + y;
}

function subtract(x, y) {
    return x - y;
}

function divide(x, y) {
    return x/y
}

function multiply(x,y) {
    return x*y
}

function modulus(x,y) {
    return x%y
}


console.log(add(4,5));
console.log(subtract(10,5));
console.log(divide(20, 9));
console.log(multiply(9,4));
console.log(modulus(8,2));
